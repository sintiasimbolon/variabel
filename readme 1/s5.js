var perkalian = 0;
var pembagian = 0;
var pertambahan = 0;
var pengurangan = 0;

function bilangan(a, b){
    perkalian = a * b;
    pembagian = a / b;
    pertambahan = a + b;
    pengurangan = a - b;
}

bilangan (3, 2);
console.log(perkalian);
bilangan (10, 2);
console.log(pembagian);
bilangan (76, 12);
console.log(pertambahan);
bilangan (789, 10);
console.log(pengurangan);